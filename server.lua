function startWork(key)
	if (key == 'XCCD') then
		triggerClientEvent(client, 'youCanWork', resourceRoot, 'XCCDplanet')
	end
end
addEvent('canIwork', true)
addEventHandler('canIwork', resourceRoot, startWork)

